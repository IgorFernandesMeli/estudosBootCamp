public class Pessoa {
    String nome;
    int idade;
    int id;
    double peso, altura;

    public Pessoa() {
        this.nome = nome;
    }

    public Pessoa(String nome, int idade, int id) {
        this.nome = nome;
        this.idade = idade;
        this.id = id;
    }

    public Pessoa(double peso, double altura) {
        this.peso = peso;
        this.altura = altura;
    }


}
